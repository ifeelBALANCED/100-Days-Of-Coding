import pytest
from week1_day6 import next_prime


@pytest.mark.parametrize("input_number, expected_next_prime_number", [
    (12, 13),
    (24, 29),
    (11, 11),
    (1, 2),
    (2, 2),
    (0, 2),
    (-1, 2),
    (17, 17),
    (18, 19),
    (1000000, 1000003),
    (999983, 999983),
    (999984, 1000003),
    (10000000, 10000019),
    (100000000, 100000007),
    (1000000000, 1000000007),
    (99999999977, 99999999977),
    (99999999978, 100000000003),
    (99999999989, 100000000003),
    (99999999990, 100000000003),
    (100000000000, 100000000003),
    (100000000019, 100000000019),
    (100000000020, 100000000057),
    (100000000033, 100000000057),
    (100000000040, 100000000057),
    (100000000067, 100000000069),
    (100000000080, 100000000091),
    (100000000093, 100000000103),
    (100000000100, 100000000103),
    (100000000119, 100000000129),
    (100000000200, 100000000211),
])
def test_next_prime(input_number: int, expected_next_prime_number: int) -> None:
    assert next_prime(input_number) == expected_next_prime_number
